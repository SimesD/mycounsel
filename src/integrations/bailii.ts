/**
 * BAILII (British and Irish Legal Information Institute) integration
 *
 * ── What is BAILII? ──────────────────────────────────────────────────────────
 *
 * BAILII (https://www.bailii.org) is the authoritative free online source for
 * British and Irish primary legal materials. It is maintained by the British
 * and Irish Legal Information Institute, a registered charity, and has operated
 * since 2000. Courts and tribunals in England & Wales, Scotland, Northern
 * Ireland, and Ireland submit judgments directly to BAILII, making it the
 * closest equivalent to an official government repository of case law.
 *
 * ── What BAILII covers ───────────────────────────────────────────────────────
 *
 * - UK Supreme Court (UKSC) — from 2009 onwards
 * - House of Lords (UKHL) — from approx. 1946 onwards
 * - Privy Council (UKPC) — selected cases from 1999 onwards
 * - Court of Appeal Civil (EWCA Civ) — from 1996 onwards
 * - Court of Appeal Criminal (EWCA Crim) — from 1996 onwards
 * - High Court divisions (EWHC) — from 1996 onwards for most divisions
 * - Upper Tribunal, Employment Appeal Tribunal, First-tier Tribunal (Tax)
 * - Older cases: selected coverage back to the 19th century, but incomplete
 *
 * ── BAILII's authority and limitations ───────────────────────────────────────
 *
 * BAILII publishes the official judgment text as handed down by the court.
 * For cases with neutral citations (introduced from 2001 onwards), BAILII is
 * the definitive source — the neutral citation IS the official reference.
 *
 * However BAILII has important limitations:
 *
 * 1. OLDER CASES: Cases before ~1996 have incomplete BAILII coverage. The
 *    authoritative sources for older cases are the commercial law reports
 *    (Weekly Law Reports, All England Law Reports, AC, QB, Ch etc.) published
 *    by Incorporated Council of Law Reporting and LexisNexis. Access to these
 *    requires a subscription (Westlaw, LexisNexis, vLex).
 *
 * 2. HOLDING SUMMARIES: BAILII publishes the full judgment only — it does not
 *    provide editorial summaries or headnotes. The holding summaries in this
 *    report are AI-generated interpretations of the judgment, not official
 *    headnotes. They must be verified against the full text of the judgment.
 *
 * 3. SUBSEQUENT TREATMENT: BAILII does not indicate whether a case has been
 *    overruled, disapproved, distinguished, or departed from by later courts.
 *    Citator services (Westlaw "Cases+" or LexisNexis "Case Analysis") must
 *    be used to check whether a cited case remains good law.
 *
 * 4. UNREPORTED CASES: Some cases are not published on BAILII. A 404 response
 *    does not definitively prove a case does not exist — it may simply not be
 *    in BAILII's database, especially for older or specialist tribunal cases.
 *
 * 5. AI HALLUCINATION RISK: The citations in this report were generated by an
 *    AI language model. Even where a direct BAILII URL is constructed and
 *    resolves successfully, the case name and holding summary must be verified
 *    against the actual judgment. AI models can generate plausible-looking but
 *    incorrect citations, transpose parties, misstate years, or fabricate cases.
 *
 * ── Verification workflow for legal professionals ─────────────────────────────
 *
 * Recommended steps before relying on any citation in this report:
 *
 *  1. Click the BAILII link — confirm the page loads and the parties match
 *  2. Read the judgment — confirm the holding stated here is accurate
 *  3. Run a citator check on Westlaw or LexisNexis — confirm the case is
 *     still good law and has not been overruled or materially distinguished
 *  4. For pre-1996 cases or cases returning 404 — search the commercial
 *     law report databases directly
 *
 * ── Supported neutral citation patterns ──────────────────────────────────────
 *
 *   [YEAR] UKSC NUMBER          → Supreme Court
 *   [YEAR] UKHL NUMBER          → House of Lords
 *   [YEAR] UKPC NUMBER          → Privy Council
 *   [YEAR] EWCA Civ NUMBER      → Court of Appeal (Civil)
 *   [YEAR] EWCA Crim NUMBER     → Court of Appeal (Criminal)
 *   [YEAR] EWHC NUMBER (Div)    → High Court with division
 *   [YEAR] EWHC NUMBER          → High Court (no division — search fallback)
 *   [YEAR] UKUT NUMBER (Chamber)→ Upper Tribunal
 *   [YEAR] UKFTT NUMBER (Tax)   → First-tier Tribunal (Tax)
 *   [YEAR] UKEAT NUMBER         → Employment Appeal Tribunal
 */

import { parseTnaCitation } from "./tna";

const BAILII_BASE = "https://www.bailii.org";

export interface CaseLink {
    /** The original citation string passed in */
    citation: string;
    /** The case name extracted from the citation (everything before the year) */
    caseName: string;
    /** The constructed or search URL */
    url: string;
    /** true = direct link to a specific judgment; false = search results page */
    isDirectLink: boolean;
    /** Human-readable label for the court */
    court: string;
    /** Source repository: "BAILII" or "TNA" */
    source: "BAILII" | "TNA";
}

export type VerificationStatus =
    | "VERIFIED" // HTTP 200 and case name found in page
    | "EXISTS" // HTTP 200 but name match inconclusive
    | "NOT_FOUND" // HTTP 404 — citation likely hallucinated or URL wrong
    | "SEARCH_ONLY" // No direct link constructed — cannot auto-verify
    | "ERROR"; // Network/timeout error during check

export interface VerifiedCaseLink extends CaseLink {
    status: VerificationStatus;
    /** Confidence 0–100 for this individual citation */
    confidence: number;
    /** Human-readable explanation of the verification result */
    verificationNote: string;
}

export interface VerificationSummary {
    links: VerifiedCaseLink[];
    /** Overall citation confidence 0–100 */
    citationConfidence: number;
    /** Number of citations that resolved on BAILII or TNA */
    verified: number;
    /** Number of citations that returned 404 */
    notFound: number;
    /** Number that could not be auto-checked (search-only or error) */
    unverifiable: number;
}

